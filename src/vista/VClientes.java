    //MODIFICAR CLIENTES EN LA BASE DE DATOS
    public void mostratabla() {

        Clientes clientes = new Clientes();
        java.util.List<Clientes> lista = clientes.mostrarDatos();
        String matriz[][] = new String[lista.size()][5];
        for (int i = 0; i < lista.size(); i++) {
            matriz[i][0] = lista.get(i).getCedula();
            matriz[i][1] = lista.get(i).getNombres();
            matriz[i][2] = lista.get(i).getApellidos();
            matriz[i][3] = lista.get(i).getCorreo();
            matriz[i][4] = lista.get(i).getTelefono();
        }
        tblClientes.setModel(new javax.swing.table.DefaultTableModel(
                matriz,
                new String[]{
                    "cedula ", "nombres", "apellidos", "correo", "telefono"
                }
        ));
    }
