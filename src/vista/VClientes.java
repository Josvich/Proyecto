    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        Clientes clientes = new Clientes(txtCedula.getText(), txtNombres.getText(), txtApellidos.getText(), txtCorreo.getText(), txtTelefono.getText());
        if (clientes.insertar()) {
            JOptionPane.showMessageDialog(rootPane, "REGISTRO GUARDADO");

            mostratabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error al guardar");

        }

    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int seleccionado = tblClientes.getSelectedRow();
        int respuesta = 0;

        if (seleccionado != -1) {
            Clientes clientes = new Clientes(txtCedula.getText(), txtNombres.getText(), txtApellidos.getText(), txtCorreo.getText(), txtTelefono.getText());
            respuesta = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea modificar el registro", "mesanje de confirmacion", JOptionPane.YES_NO_OPTION);
            if (respuesta == 0) {

                if (clientes.modificar()) {
                    JOptionPane.showMessageDialog(rootPane, "Registro guardado");
                    mostratabla();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error al guardar");
                }
            }
        }

    }//GEN-LAST:event_btnModificarActionPerformed

