    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        Empleados empleados = new Empleados(txtCedula.getText(), txtNombres.getText(), txtApellidos.getText(), txtUsuario.getText(), txtContraseña.getText(), txtRepitacontraseña.getText(), txtCorreo.getText(), txtTelefono.getText(), cmbRol.getSelectedItem().toString());
        if (empleados.insertar()) {
            JOptionPane.showMessageDialog(rootPane, "REGISTRO GUARDADO");
            mostratabla();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Error al guardar");

        }

    }//GEN-LAST:event_btnInsertarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        int seleccionado = tblEmpleados.getSelectedRow();
        int respuesta = 0;

        if (seleccionado != -1) {
            Empleados empleados = new Empleados(txtCedula.getText(), txtNombres.getText(), txtApellidos.getText(), txtUsuario.getText(), txtContraseña.getText(), txtRepitacontraseña.getText(), txtCorreo.getText(), txtTelefono.getText(), cmbRol.getSelectedItem().toString());
            respuesta = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea modificar el registro", "mesanje de confirmacion", JOptionPane.YES_NO_OPTION);
            if (respuesta == 0) {

                if (empleados.modificar()) {
                    JOptionPane.showMessageDialog(rootPane, "Registro guardado");
                    mostratabla();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Error al guardar");
                }
            }
        }

    }//GEN-LAST:event_btnModificarActionPerformed

