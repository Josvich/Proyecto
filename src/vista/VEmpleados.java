    private void txtCedulaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCedulaFocusLost
        validaciones vali = new validaciones();
        boolean resultado = vali.validacedula(txtCedula.getText());
        if (resultado == false) {
            JOptionPane.showMessageDialog(rootPane, "Cedula repetida o ingreso caracteres no permitidos", "Validacion de la cedula", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_txtCedulaFocusLost

